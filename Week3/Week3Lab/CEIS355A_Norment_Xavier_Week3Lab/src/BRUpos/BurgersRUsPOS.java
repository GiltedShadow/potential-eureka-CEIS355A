    private void txtQuantityFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtQuantityFocusLost
        //Runs when the user clicks off quantity to check if it is valid
        
        if (txtQuantity.getText().equals("")){
            txtQuantity.setText(String.valueOf(1));
            JOptionPane.showMessageDialog(rootPane, "Please enter a whole number for the quantity");
        }
        else if (Integer.parseInt(txtQuantity.getText())<=0){
            txtQuantity.setText(String.valueOf(1));
            JOptionPane.showMessageDialog(rootPane, "Please enter a positive whole number for the quantity");
        }
    }//GEN-LAST:event_txtQuantityFocusLost

    private void clearGUI(){
        //clears out nearly all gui inputs
        rbtnThisIsNotVisible.setSelected(true);
        cboxCheese.setSelected(false);
        cboxBacon.setSelected(false);
        cboxMeal.setSelected(false);
        txtQuantity.setText(String.valueOf(1));
        displayPrice();
    }
    private void updateOrderTotal(double priceToAdd){
        //adds the current order to the total order price then updates the display
        orderTotalPrice += priceToAdd;
        txtOrderTotal.setText(String.format("$%.2f", orderTotalPrice));
    }
    private void displayPrice(){
        //pulls all inputs and upadted item price
        priceReset();
        if (rbtnSBurger.isSelected()){totalPrice += singlePrice;}
        else if (rbtnDBurger.isSelected()){totalPrice += doublePrice;}
        if (cboxCheese.isSelected()){totalPrice += cheesePrice;}
        if (cboxBacon.isSelected()){totalPrice += baconPrice;}
        if (cboxMeal.isSelected()){totalPrice += mealPrice;}
        txtItemPrice.setText(String.format("$%.2f", totalPrice));
    }
    private void priceReset(){
        totalPrice = 0.0;
    }
