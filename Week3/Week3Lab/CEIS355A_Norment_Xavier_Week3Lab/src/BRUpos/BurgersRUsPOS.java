    private void mitemAddToOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mitemAddToOrderActionPerformed
        /*
        pulls the currently selected order
        adds the current order to the order box
        updates the order total
        clears the gui
        */
        
        String custOrder = "";
        int quantity = Integer.parseInt(txtQuantity.getText());
        if (rbtnThisIsNotVisible.isSelected()){
            JOptionPane.showMessageDialog(rootPane, "Please select either a single or double burger");
        }
        if (quantity>1){
            updateOrderTotal(totalPrice*quantity);
            if (rbtnSBurger.isSelected()){
                custOrder = String.format("%s singles", txtQuantity.getText());
            }
            else if (rbtnDBurger.isSelected()){
                custOrder = String.format("%s doubles", txtQuantity.getText());
            }
        }
        else {
            updateOrderTotal(totalPrice);
            if (rbtnSBurger.isSelected()){
                custOrder = "1 single";
            }
            else if (rbtnDBurger.isSelected()){
                custOrder = "1 double";
            }
        }
        if (cboxCheese.isSelected() && cboxBacon.isSelected()){
            custOrder = custOrder + " with cheese and bacon";
        }
        else if (cboxCheese.isSelected()){
            custOrder = custOrder + " with cheese";
        }
        else {
            custOrder = custOrder + " with bacon";
        }
        if (cboxMeal.isSelected()){
            custOrder = custOrder + ", meal";
        }
        custOrder += String.format(" at $%.2f each", totalPrice);
        clearGUI();
        tareaYourOrder.append(custOrder+"\n");
    }//GEN-LAST:event_mitemAddToOrderActionPerformed

